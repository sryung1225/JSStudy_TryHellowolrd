var a=1;
console.log(a); //1
console.log(a+1); //2
console.log(a); //1

var b=1;
console.log(b); //1
console.log(++b); //2
console.log(b); //2

var c=1;
console.log(c); //1
console.log(--c); //0
console.log(c); //0

var d=1;
console.log(d); //1
console.log(d++); //1
console.log(d); //2

console.log(Math.pow(2, 3)); //2의 3제곱 = 8
console.log(Math.sqrt(16)); //루트16 = 4
console.log(Math.random()); //0~1 사이 랜덤 수